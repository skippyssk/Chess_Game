
def BR1_position(chessboard):
    i = 0
    j = 0
    while j <= 7:
        i = 0
        while i <= 7:
            if chessboard[j][i] == "BR1":
               
                BR1position = [j, i]
                return BR1position
                break
            else:
                i += 1
        j += 1
    return None

